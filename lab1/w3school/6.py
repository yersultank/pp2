#Python Variables - Assign Multiple Values
#Python allows you to assign values to multiple variables in one line:
x,y,z=1,2,3
print(x)
print(y)
print(z)
#you can assign the same value for multiple variables in one line:
x=y=z="hi"
print(x)
print(y)
print(z)
#unpacking a list:
unis=["KBTU","SDU","MIT"]
x,y,z=unis
print(x)
print(y)
print(z)